package com.tulin.v8.fuzzyxml;

public class FuzzyXMLException extends RuntimeException {

	private static final long serialVersionUID = -5038470190520283797L;

	public FuzzyXMLException() {
		super();
	}

	public FuzzyXMLException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FuzzyXMLException(String arg0) {
		super(arg0);
	}

	public FuzzyXMLException(Throwable arg0) {
		super(arg0);
	}
	
}
